    private void actualizarMonedas(Map<String, Double> conversionRates) {
        jMonedaPrincipal.removeAllItems();
        jMonedaConvertir.removeAllItems();

        for (Map.Entry<String, Double> entry : conversionRates.entrySet()) {
            String moneda = entry.getKey();
            Double valor = entry.getValue();
            jMonedaPrincipal.addItem(moneda + ": " + valor);
            jMonedaConvertir.addItem(moneda + ": " + valor);
        }
    }

    public void mostrarResultado(double resultado) {
        txtRes.setText(String.format("%.2f", resultado));
    }

