    private void btnConvertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConvertirActionPerformed
        String monedaPrincipal = obtenerNombreMonedaSeleccionada(jMonedaPrincipal);
        String monedaConvertir = obtenerNombreMonedaSeleccionada(jMonedaConvertir);

        double valorAConvertir;
        try {
            valorAConvertir = Double.parseDouble(txtConversion.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese un valor numérico válido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            double resultado = controladorConversor.convertirMoneda(monedaPrincipal, monedaConvertir, valorAConvertir);
            txtRes.setText(String.format("%.2f", resultado));
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnConvertirActionPerformed

